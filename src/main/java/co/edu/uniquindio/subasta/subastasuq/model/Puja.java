package co.edu.uniquindio.subasta.subastasuq.model;

public class Puja {
    private String codigoPuja;
    private String ofertaInicial;
    private String fechaPuja;

    public Puja() {
    }

    public String getCodigoPuja() {
        return codigoPuja;
    }

    public void setCodigoPuja(String codigoPuja) {
        this.codigoPuja = codigoPuja;
    }

    public String getOfertaInicial() {
        return ofertaInicial;
    }

    public void setOfertaInicial(String ofertaInicial) {
        this.ofertaInicial = ofertaInicial;
    }

    public String getFechaPuja() {
        return fechaPuja;
    }

    public void setFechaPuja(String fechaPuja) {
        this.fechaPuja = fechaPuja;
    }
}
